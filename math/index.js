export const add = (x, y) => {
  return x + y;
};

export const subtract = (x, y) => {
  return x - y;
};

export const divide = (x, y) => {
  return x / y;
};

export const multiply = (x, y) => {
  return x * y;
};

// module.exports = { add, subtract, multiply, divide };
export default { add, subtract, multiply, divide };
